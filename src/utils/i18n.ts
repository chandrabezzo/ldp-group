export type Language = 'id' | 'en';
export type TranslationKey = string;

/**
 * Get the current language from the URL path
 */
export function getLanguageFromURL(pathname: string): Language {
  const segments = pathname.split('/').filter(Boolean);
  if (segments.length > 0 && segments[0] === 'en') {
    return 'en';
  }
  return 'id'; // Default to Indonesian
}

/**
 * Get a translation by key path (e.g., "nav.about")
 */
export function t(key: string, lang: Language = 'id'): string {
  const keys = key.split('.');
  let value: any = translations[lang];
  
  for (const k of keys) {
    if (value && typeof value === 'object' && k in value) {
      value = value[k];
    } else {
      console.warn(`Translation key not found: ${key} for language: ${lang}`);
      return key;
    }
  }
  
  return typeof value === 'string' ? value : key;
}

/**
 * Create a translation function bound to a specific language
 */
export function createTranslator(lang: Language) {
  return (key: string) => t(key, lang);
}

/**
 * Get the alternate language URL path
 */
export function getAlternateLanguagePath(currentPath: string, targetLang: Language): string {
  const currentLang = getLanguageFromURL(currentPath);
  
  if (currentLang === targetLang) {
    return currentPath;
  }
  
  // Remove /en prefix if switching to Indonesian
  if (targetLang === 'id') {
    return currentPath.replace(/^\/en(\/|$)/, '/') || '/';
  }
  
  // Add /en prefix if switching to English
  if (targetLang === 'en') {
    if (currentPath === '/' || currentPath === '') {
      return '/en';
    }
    return `/en${currentPath}`;
  }
  
  return currentPath;
}

